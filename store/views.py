def login_view(request):
    if request.method == 'POST':
        form = CustomLoginForm(request=request, data=request.POST)
        if form.is_valid():
            username = form.cleaned_data.get('username')
            password = form.cleaned_data.get('password')

            user = authenticate(request, username=username, password=password)

            if user is not None:
                login(request, user)
                return redirect('store')  # Or wherever you want the user to go after login
            else:
                form.add_error(None, 'Invalid username or password')
    else:
        form = CustomLoginForm()

    return render(request, 'registration/login.html', {'form': form})


def signup(request):
    if request.method == 'POST':
        form = CustomUserCreationForm(request.POST)
        if form.is_valid():
            user = form.save()  # Save the user object
            # Create the associated Customer object for the new user
            Customer.objects.create(user=user)  # Ensure the customer is created
            return redirect('login')  # Redirect the user to login after successful sign-up
    else:
        form = CustomUserCreationForm()

    return render(request, 'registration/signup.html', {'form': form})

